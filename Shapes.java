
package Inheritance;

public class Shapes {
    private int length=10;
    private int wrigth=20;
    private int height=2;
    private int base=5;

    public Shapes(int length, int wrigth, int height, int base) {
        this.length = length;
        this.wrigth = wrigth;
        this.height = height;
        this.base = base;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getWrigth() {
        return wrigth;
    }

    public void setWrigth(int wrigth) {
        this.wrigth = wrigth;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }
    
    
        
    
    
    
}
